#[test]
fn policy_scenario_matrix_covers_yanks_and_toolchain_floors() {
    let requirement = VersionReq::parse("^1").unwrap();
    for scenario in POLICY_SCENARIOS {
        let release = CrateRelease {
            name: scenario.name.to_owned(),
            version: Version::new(scenario.version.0, scenario.version.1, scenario.version.2),
            checksum: format!("{:064x}", scenario.version.1 * 10 + scenario.version.2),
            yanked: scenario.yanked,
            rust_version: Some(Version::new(
                scenario.required_rust.0,
                scenario.required_rust.1,
                scenario.required_rust.2,
            )),
        };
        let workspace = Version::new(
            scenario.workspace_rust.0,
            scenario.workspace_rust.1,
            scenario.workspace_rust.2,
        );
        assert_eq!(
            crate::release_allowed(&release, &requirement, &workspace),
            scenario.expected,
            "{}",
            scenario.name,
        );
    }
}
